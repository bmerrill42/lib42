#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*s;

	s = s1;
	while (*s != '\0')
		++s;
	while ((*s = *s2) != '\0')
	{
		++s;
		++s2;
	}
	return (s1);
}
