#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;

	s = s1;
	while (*s != '\0')
		++s;
	while (*s2 != '\0' && 0 < n)
	{
		*s++ = *s2++;
		--n;
	}
	*s = '\0';
	return (s1);
}
